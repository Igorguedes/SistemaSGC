    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        try {
          
            boolean status = lerDadosTela();
            if (status == true) {
                ProdutoBO produtoBO = new ProdutoBO();
                produtoBO.atualizarDadosProduto(produto);
                exibirMensagemSucesso();
            } else {
                exibirMensagemErro();
            }
        } catch (ParseException ex) {
            Logger.getLogger(TelaEditarProduto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaEditarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed
    public boolean lerDadosTela() throws ParseException {
        produto = new Produto();

        String nome = txtNome.getText().trim();
        String preco = txtPreco.getText();
        String quantidade = txtQuantidade.getText();
        if (nome.isEmpty() || preco.isEmpty() || quantidade.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Impossivel cadastrar com algum dos campos estando vazios!", "Erro ao cadastrar produto", JOptionPane.ERROR_MESSAGE);
            return false;

        } else {
            produto.setNome(txtNome.getText().trim());
            DecimalFormat formatador = new DecimalFormat("#,##0.00");
            produto.setPreco(formatador.parse(preco).doubleValue());
            produto.setQuantidade(Integer.parseInt(quantidade));
            return true;

        }

    }

    public void setValoresTela() {

        try {
            txtNome.setText(this.produto.getNome().trim());
            txtPreco.setText(String.valueOf(this.produto.getPreco()));
            txtQuantidade.setText(Integer.toString(this.produto.getQuantidade()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar valores", "Erro ao alterar produto", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void exibirMensagemErro() {
        String titulo = "Erro ao alterar produto!";
        String mensagem = "Erro desconhecido. Consulte o administrador do sistema!";
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
    }

    private void exibirMensagemSucesso() {
        String titulo = "Alterar produto!";
        String mensagem = "Produto Alterado com sucesso!";
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }

