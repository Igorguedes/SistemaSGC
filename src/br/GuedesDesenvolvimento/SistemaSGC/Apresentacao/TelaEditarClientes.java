    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        
        boolean status = lerDadosTela();
        try {

            if (status == true) {        
                ClienteBO clienteBO = new ClienteBO();
                clienteBO.atualizarDados(cliente);
                exibirMensagemSucesso();
                this.dispose();
            } else {
                exibirMensagemErro();
            }
        } catch (SQLException ex) {
            exibirMensagemErro();
            Logger.getLogger(TelaEditarClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    public void setValoresCampos() {
        try {
            txtNome.setText(this.cliente.getNome());
            txtCPF.setText(this.cliente.getCPF());
            txtTelefone.setText(this.cliente.getTelefone());
            txtEndereco.setText(this.cliente.getEndereco());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar valores", "Erro ao alterar cliente", JOptionPane.ERROR_MESSAGE);
        }
    }

    public boolean lerDadosTela() {
        String nome = txtNome.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String endereco = txtEndereco.getText().trim();
        String CPF = txtCPF.getText().trim();
        String mensagem = "Impossivel cadastrar com algum dos campos estando vazios!";

        if ((nome.isEmpty()) || (telefone.isEmpty()) || (endereco.isEmpty()) || (CPF.isEmpty())) {
            JOptionPane.showMessageDialog(this, mensagem, "Erro ao cadastrar cliente", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            cliente = new Cliente();
            this.cliente.setNome(txtNome.getText().trim());
            this.cliente.setEndereco(txtEndereco.getText().trim());
            this.cliente.setTelefone(txtTelefone.getText().trim());
            this.cliente.setCPF(txtCPF.getText().trim());
            return true;
        }
    }

    private void exibirMensagemSucesso() {
        String titulo = "Alterar cliente!";
        String mensagem = "Cliente alterado com sucesso!";
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }

    private void exibirMensagemErro() {
        String titulo = "Erro ao alterar cliente!";
        String mensagem = "Erro desconhecido. Consulte o administrador do sistema!";
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
    }

